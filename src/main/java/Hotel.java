public class Hotel {
    private final String hotelName;
    private final int level;

    public Hotel(String hotelName, int level) {
        this.hotelName = hotelName;
        this.level = level;
    }

    public String getHotelName() {
        return hotelName;
    }

    public int getLevel() {
        return level;
    }

}
